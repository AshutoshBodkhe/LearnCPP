#include <iostream>

int main() {
    std::cout << 5 << std::endl; // Output: 5
    std::cout << 5u << std::endl; // Output: 5
    std::cout << 5L << std::endl; // Output: 5
    std::cout << 5UL << std::endl; // Output: 5
    std::cout << 5LL << std::endl; // Output: 5
    std::cout << 5ULL << std::endl; // Output: 5
    std::cout << 5.0f << std::endl; // Output: 5.0
    std::cout << 5.0 << std::endl; // Output: 5.0
    return 0;

}